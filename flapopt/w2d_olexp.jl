function olExpPlot(V, stroke)
	Nv = length(V)
	p = plot(xlabel="Freq [Hz]", ylabel="Norm. stroke ampl [deg/V]", ylims=(0.2,0.7))
	for i=1:Nv
		plot!(p, stroke[i][:,1], stroke[i][:,2]/V[i], label=V[i], markershape=:auto, lw=2)
	end
	return p
end

